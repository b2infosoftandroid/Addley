public class Coupons {
    private String couponID;
    private String couponName;
    private String couponImage;
    private String couponOfferId;
    private String couponOfferCode;
    private String couponOfferUrl;
    private String couponOfferTitle;
    private String couponOfferEndDate;
    private String couponOfferCatDate;
    private String lastPosition;
    private String off_code;
    public String getCouponID() {
        return couponID;
    }

    public void setCouponID(String couponID) {
        this.couponID = couponID;
    }

    public String getCouponName() {
        return couponName;
    }

    public void setCouponName(String couponName) {
        this.couponName = couponName;
    }

    public String getCouponImage() {
        return couponImage;
    }

    public void setCouponImage(String couponImage) {
        this.couponImage = couponImage;
    }

    public String getCouponOfferId() {
        return couponOfferId;
    }

    public void setCouponOfferId(String couponOfferId) {
        this.couponOfferId = couponOfferId;
    }

    public String getCouponOfferCode() {
        return couponOfferCode;
    }

    public void setCouponOfferCode(String couponOfferCode) {
        this.couponOfferCode = couponOfferCode;
    }

    public String getCouponOfferUrl() {
        return couponOfferUrl;
    }

    public void setCouponOfferUrl(String couponOfferUrl) {
        this.couponOfferUrl = couponOfferUrl;
    }

    public String getCouponOfferTitle() {
        return couponOfferTitle;
    }

    public void setCouponOfferTitle(String couponOfferTitle) {
        this.couponOfferTitle = couponOfferTitle;
    }

    public String getCouponOfferEndDate() {
        return couponOfferEndDate;
    }

    public void setCouponOfferEndDate(String couponOfferEndDate) {
        this.couponOfferEndDate = couponOfferEndDate;
    }

    public String getCouponOfferCatDate() {
        return couponOfferCatDate;
    }

    public void setCouponOfferCatDate(String couponOfferCatDate) {
        this.couponOfferCatDate = couponOfferCatDate;
    }

    public String getLastPosition() {
        return lastPosition;
    }

    public void setLastPosition(String lastPosition) {
        this.lastPosition = lastPosition;
    }

    public String getOff_code() {
        return off_code;
    }

    public void setOff_code(String off_code) {
        this.off_code = off_code;
    }
}
